/** # Matrix #
 * @abbrev mtx
 * Wrapper class for PIXI.js matrix. Construct with <js>new Matrix()</js>
 * $$ mtx = gfx.Matrix() $$
 * @returns obj-Matrix
*/




// ------------- VISIBLE METHODS & PROPERTIES -------------


/** ### mtx
 * @prop
 * The PIXI.js Matrix
 * @returns obj-Pixi Matrix
 */


/** ### Translate ###
 * Move by a given amount on both axes
 * $$ mtx.Translate(tx, ty) $$
 * @param {num} tx
 * @param {num} ty
 */


/** ### Rotate ###
 * Rotate by a given angle around the axes origin (0,0)
 * $$ mtx.Rotate(angle) $$
 * @param {num} angle
 */


/** ### Scale ###
 * Scale by a given amount on both axes
 * $$ mtx.Scale(sx, sy) $$
 * @param {num} sx
 * @param {num} sy
 */


/** ### Set ###
 * Set the matrix elements to specific values
 * $$ mtx.Set(a, b, c, d, tx, ty) $$
 * @param {num} a
 * @param {num} b
 * @param {num} c
 * @param {num} d
 * @param {num} tx
 * @param {num} ty
 */


/** ### Skew ###
 * Skew / Distort the object on both axes
 * $$ mtx.Skew(skewX, skewY) $$
 * @param {num} skewX
 * @param {num} skewY
 */


/** ### Transform ###
 * Shortcut to app.@../app/Vibrate)
 * $$ mtx.Transform(tx, ty, pivotX, pivotY, scaleX, scaleY, rotation, skewX, skewY) $$
 * @param {num} tx
 * @param {num} ty
 * @param {num} pivotX
 * @param {num} pivotY
 * @param {num} scaleX
 * @param {num} scaleY
 * @param {num} rotation
 * @param {num} skewX
 * @param {num} skewY
 */
