/** # GetName #
 * @brief Get APK name
 * Returns the name of the APK application. meaning if your app runs in DroidScript, the name will be “DroidScript” as well.
 * $$ app.GetName() $$
 * @returns str
*/




// ------------- SAMPLES -------------



/**
@sample Get app Name
function OnStart()
{
	<b>name = app.GetName();</b>
	app.Alert( name );
}
 */



